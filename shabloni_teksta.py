#Майн описание игры---> Minecraft — это приключенческая игра‑песочница с элементами выживания и RPG. Её ключевая особенность — отсутствие жёсткого сюжета и ограничений: игрок сам определяет цели и способы их достижения. Игру часто сравнивают с конструктором LEGO: мир и объекты в ней состоят из трёхмерных блоков, которые можно добывать и использовать для строительства.#




















































